    private void TampilData(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("KD PELANGGAN");
        model.addColumn("NAMA PELANGGAN");
        model.addColumn("HARGA");
        model.addColumn("JENIS LAUNDRY");
        jTable1.setModel(model);
        
        try {
            java.sql.Statement stat = koneksi.createStatement();
            ResultSet data = stat.executeQuery("SELECT * FROM home");
            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString("kd_pelanggan"),
                    data.getString("nama_pelanggan"),
                    data.getString("harga"),
                    data.getString("jenis_laundry"),
                });
                jTable1.setModel(model);
            }
        } catch (Exception e){
            System.err.println("terjadi kesalahan :" + e);
        }
    }
    
