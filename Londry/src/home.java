    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String kd_pelanggan = jTextField1.getText();
        String nama_pelanggan = jTextField2.getText();
        String harga = jTextField3.getText();
        String jenis_laundry = (String) jComboBox1.getSelectedItem();
        
        if (jTextField1.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "silahkan isi data untuk di masukan", "PERHATIAN", JOptionPane.WARNING_MESSAGE); 
        } else {
               try {
                   java.sql.Statement stat = koneksi.createStatement();
                   ResultSet data = stat.executeQuery("SELECT * FROM home WHERE kd_pelanggan ='" + jTextField1.getText() + "'");
                   if (data.next ()) {
                        JOptionPane.showMessageDialog(null, "kode sudah di gunakan", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                        jTextField1.requestFocus();
                    } else {
                      String sql = "INSERT INTO home VALUES('" + kd_pelanggan + "'"
                              + ",'" + kd_pelanggan + "'"
                              + ",'" + nama_pelanggan + "'"
                              + ",'" + harga + "'"
                              + ",'" + jenis_laundry + "')";
                      stat.executeUpdate(sql);
                      jTextField1.setText("");
                      jTextField2.setText("");
                      jTextField3.setText("");
                      jComboBox1.setSelectedIndex(-0);
                      jTextField1.requestFocus();
                      JOptionPane.showMessageDialog(null, "data berhasil di simpan", "SUKSES", JOptionPane.WARNING_MESSAGE);
                      TampilData();
                    }
                    stat.close();
                } catch (Exception exc) {
                    System.err.println("terjadi kesalahan :" + exc);
                      }
                    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

